namespace LabOfKiwi.Html.Attributes;

/// <summary>
/// Controls the behaviour of a button when it is activated.
/// </summary>
public enum ButtonType
{
    /// <summary>
    /// Submits the form.
    /// </summary>
    Submit,

    /// <summary>
    /// Resets the form.
    /// </summary>
    Reset,

    /// <summary>
    /// Does nothing.
    /// </summary>
    Button
}
